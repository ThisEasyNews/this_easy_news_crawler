    POLITICS = "CAT_POLITICS"
    ECONOMY = "CAT_ECONOMY"
    SOCIETY = "CAT_SOCIETY"
    INTERNATIONAL = "CAT_INTERNATIONAL"
    SPORTS = "CAT_SPORTS"
    CULTURE = "CAT_CULTURE"
    ENTERTAINMENT = "CAT_ENTERTAINMENT"
    TECH_SCIENCE = "CAT_TECH_SCIENCE"

